let x = 1;

// Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

// Equality
console.log(x === 1);
console.log(x !== 1);

console.log("===============Divider===============");

// Strict Equality (Type + Value)
console.log(1 === 1);
console.log("1" === 1);

// Loose Equality (Type)
console.log(1 == 1);
console.log("1" == 1);
console.log(true == 1);
console.log(true == 2);
